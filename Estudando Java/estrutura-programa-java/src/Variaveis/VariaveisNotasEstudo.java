package Variaveis;
/*
 * aqui é só um comentario, essa aula é uma aula pratuca de variaveis no Java
 * As variaveis são conteiners de dados
 */
public class VariaveisNotasEstudo {
    public static void main(String[] args) {
        // Variaveis que reperesentam a data atual
        int dia = 05; 
        int mes = 11; 
        int ano = 25;

        // atribuir novo valor para o dia 
        dia = 06;


        // impressão da data atual
        System.out.println(dia);
        System.out.println(mes);
        System.out.println(ano);
    }
    public static void Outrometodo(String[] args) {
        int dia = 06;
        System.out.println(dia);

    }
}

